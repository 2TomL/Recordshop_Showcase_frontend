    function setLang(lang) {
      document.documentElement.lang = lang;
      // Teksten
      document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) el.textContent = translations[lang][key];
      });
      // Placeholders
      document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) el.placeholder = translations[lang][key];
      });
    }

    // Language switcher events for <select> dropdowns
    document.addEventListener('DOMContentLoaded', function() {
      var langSelect = document.getElementById('langSelect');
      if (langSelect) {
        langSelect.addEventListener('change', function() {
          setLang(this.value);
        });
      }
      var mobileLangSelect = document.getElementById('mobileLangSelect');
      if (mobileLangSelect) {
        mobileLangSelect.addEventListener('change', function() {
          setLang(this.value);
        });
      }
      // Set default language and dropdowns
      setLang('en');
      if (langSelect) langSelect.value = 'en';
      if (mobileLangSelect) mobileLangSelect.value = 'en';
    });